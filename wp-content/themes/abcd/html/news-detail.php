                <div class="desc">
                    <p>GREAT’s business and education partners also provided an update on the establishment of business
                        services and support for the agriculture and tourism sectors in the northwest, including
                        improved access to finance, digital inclusion and the availability of market-driven skills
                        training. All of these services have been designed with the needs of women in mind, including
                        issues such as language and literacy.</p>
                    <p>The skills of just some of these women were shared with all in the evening market, cooking show
                        and cultural performances. Symposium attendees had the opportunity to sample the products,
                        produce and experiences that the governments, communities and businesses of Son La and Lao Cai
                        aspire to harness as they pursue inclusive, sustainable economic development.</p>
