                    <p>Throughout the day, the GREAT program team and Government of Vietnam representatives discussed
                        evidence and lessons learned from the GREAT Program that can help inform Vietnam’s National
                        Target Programs (NTPs)implementation. The NTPs and GREAT both aspire to stronger socio-economic
                        opportunities in Vietnam’s northwest region, particularly for the ethnic communities that call
                        the region home.</p>
                    <p>GREAT is Australia’s most significant gender investment in Vietnam, totalling A$33.7 million. The
                        program works with businesses, NGOs, government and civil society partners to build more
                        inclusive business and market systems within the agriculture and tourism sectors in Son La and
                        Lao Cai provinces. It also works to ensure that local women and ethnic minorities actively
                        participate in and benefit from related economic activities and growth.</p>
                    <p>The GREAT Program began in 2017 and is now entering into a second phase which will see the
                        Program extend out to 2027. It is implemented in partnership with the Son La and Lao Cai
                        Provincial Governments and the Symposium was an important step in further cementing these
                        partnerships.</p>
                    <p>Mr Nguyen Thanh Hai from the Son La Provincial Government and Mr Le Hong Phong from the Lao Cai
                        Provincial Government both shared the experience of delivering GREAT, particularly the lessons
                        learned in relation to improving the agriculture and tourism sectors while actively working to
                        promote the economic empowerment of women.</p>
                </div>
