                        Nationwide in Hanoi on 27 May 2022.</p>
                    <p>Symposium attendees included Australian and Vietnamese government representatives, the private
                        sector and civil society organisations. Ethnic minority women were also represented and the
                        event concluded with an evening market and cooking show.</p>
